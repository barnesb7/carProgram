package com.company;

public class Car {

    private String color;
    private String make;
    private String model;
    private int year;


    public void setColor(String carColor){
        color = carColor;
    }

    public void setMake(String carMake){
        make = carMake;
    }

    public void setModel(String carModel){
        model = carModel;
    }

    public void setYear(int carYear){
        year = carYear;
    }

}
